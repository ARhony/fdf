#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		length;
	char	*s2;

	i = 0;
	length = ft_strlen((char*)s1);
	if ((s2 = malloc(sizeof(char) * length + 1)) == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
