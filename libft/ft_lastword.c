#include "libft.h"

char	*ft_lastword(char *str)
{
	int i;
	int c;

	i = 0;
	c = 1;
	while (str[i] != '\0')
		i++;
	i -= 1;
	while (str[i] == ' ' || str[i] == '\t')
		i--;
	while (str[i] != ' ' && str[i] != '\t' && str[i])
		i--;
	i += 1;
	return (str + i);
}
